#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	if (!lst || !del)
		return ;
	temp = *lst;
	while (*lst)
	{
		*lst = (*lst)->next;
		del((temp)->content);
		free(temp);
		temp = *lst;
	}
	*lst = NULL;
}
